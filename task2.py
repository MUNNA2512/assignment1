# Task 2: Create a Personalized Greeting

# Taking user's first name and last name as input
first_name = input("Enter your first name: ")
last_name = input("Enter your last name: ")

# Concatenating first name and last name into a full name
full_name = first_name + " " + last_name

# Printing a personalized greeting message
print(f"Hello, {full_name}! Welcome to our Python program with tutedude.") 